class User < ApplicationRecord
  # password及びpassword_confirmationの属性が追加
  has_secure_password

  validates :name,
    presence: true,
    uniqueness: true,
    length: { maximum: 16 },
    format: {
      with: /\A[a-z0-9]+\z/,
      message: 'は小文字英数字で入力をして下さい'
    }
    validates :password,
      length: { minimum: 8 }
end
